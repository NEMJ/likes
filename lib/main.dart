import 'package:flutter/material.dart';
import 'package:firebase_core/firebase_core.dart';
import 'package:likes/fire_page.dart';
import 'firebase_options.dart';

// Link do tutorial para uso do RealTime Database do Firebase
// https://www.raywenderlich.com/24346128-firebase-realtime-database-tutorial-for-flutter

void main() async {
  WidgetsFlutterBinding.ensureInitialized();

  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({Key? key}) : super(key: key);

  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      title: 'Flutter Demo',
      theme: ThemeData(
        useMaterial3: true,
        colorSchemeSeed: const Color.fromARGB(0, 6, 51, 111),
        brightness: Brightness.dark,
      ),
      home: const FirePage(),
      // home: const MyHomePage(title: 'Flutter Demo Home Page'),
    );
  }
}

